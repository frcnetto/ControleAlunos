package br.estacio.dsw.model;

public class ProvaAbstrata {
	private long id;
	private int peso;
	
	public ProvaAbstrata(){
		
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}
}
